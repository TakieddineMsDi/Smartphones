$('#toggle-account').click(function(){
	$('#account').css( "visibility", "visible" );
  $('#account').toggle();
});

$('#toggle-login').click(function(){
	$('#login').css( "visibility", "visible" );
	  $('#login').toggle();
	});

$('#toggle-signup').click(function(){
	$('#signup').css( "visibility", "visible" );
	  $('#signup').toggle();
	});